    private String getStateRep(char[][] state){
        StringBuilder value = new StringBuilder();
        for (char[] row : state) {
            for (char cell : row) {
                value.append(cell);
            }
            value.append('\n');
        }
        return value.toString();
    }//end getStateRep
    
